<?php

namespace App\Entity;

use App\Repository\ProductsRepository;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=ProductsRepository::class)
 */
class Products
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $name;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $category;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $description;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $picture;

    /**
     * @ORM\ManyToOne(targetEntity=Manufacturer::class)
     */
    public $fk_manufacturer;

    /**
     * @ORM\ManyToOne(targetEntity=Price::class)
     */
    public $fk_price;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }

    public function getCategory(): ?string
    {
        return $this->category;
    }

    public function setCategory(string $category): self
    {
        $this->category = $category;

        return $this;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

    public function setDescription(string $description): self
    {
        $this->description = $description;

        return $this;
    }

    public function getPicture(): ?string
    {
        return $this->picture;
    }

    public function setPicture(string $picture): self
    {
        $this->picture = $picture;

        return $this;
    }

    public function getFkManufacturer(): ?Manufacturer
    {
        return $this->fk_manufacturer;
    }

    public function setFkManufacturer(?Manufacturer $fk_manufacturer): self
    {
        $this->fk_manufacturer = $fk_manufacturer;

        return $this;
    }

    public function getFkPrice(): ?Price
    {
        return $this->fk_price;
    }

    public function setFkPrice(?Price $fk_price): self
    {
        $this->fk_price = $fk_price;

        return $this;
    }
}
